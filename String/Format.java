public class Format {
    public static void main(String[] args) {  
        String n1 = String.format("%d", 5);            
        String n2 = String.format("%s", "Priti"); 
        String n3 = String.format("%f", 5.290);         
        String n4 = String.format("%x", 15);          
        String n5 = String.format("%c", 'p');           
        System.out.println(n1);  
        System.out.println(n2);  
        System.out.println(n3);  
        System.out.println(n4);  
        System.out.println(n5);  
    }  
}
