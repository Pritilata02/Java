public class Concat {
    public static void main(String[] args) {      
        String str1 = "My Name is";  
        String str2 = " Pritilata";  
        String str3 = " Samanta";    
        String str4 = str1.concat(str2);          
        System.out.println(str4);    
        String str5 = str1.concat(str2).concat(str3);  
        System.out.println(str5);  
    }  
}  

